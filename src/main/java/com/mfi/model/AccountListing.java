package com.mfi.model;

public class AccountListing {
	private String id;
	private String name;
	private String cuNumber;
	private double cuAmount;
	private String saNumber;
	private double saAmount;
	private String loNumber;
	private double loAmount;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCuNumber() {
		return cuNumber;
	}
	public void setCuNumber(String cuNumber) {
		this.cuNumber = cuNumber;
	}
	public double getCuAmount() {
		return cuAmount;
	}
	public void setCuAmount(double cuAmount) {
		this.cuAmount = cuAmount;
	}
	public String getSaNumber() {
		return saNumber;
	}
	public void setSaNumber(String saNumber) {
		this.saNumber = saNumber;
	}
	public double getSaAmount() {
		return saAmount;
	}
	public void setSaAmount(double saAmount) {
		this.saAmount = saAmount;
	}
	public String getLoNumber() {
		return loNumber;
	}
	public void setLoNumber(String loNumber) {
		this.loNumber = loNumber;
	}
	public double getLoAmount() {
		return loAmount;
	}
	public void setLoAmount(double loAmount) {
		this.loAmount = loAmount;
	}
	
	

}
